'''
Python = linguagem de programação
Tipo de tipagem = dinamina/forte
str -> string = texto
Strings são textos que estão dentro das aspas
'''

#impressão de numeros
print(1234)

#impressão de strings , aspas simples
print('joao')

#aspas duplas
print("jose")

#escape
print('jose \'otavio\'') # vai exibir as aspas em volta do nome otavio
print(r'jose \'otavio\'') # vai exibir as aspas e as barras invertidas em volta do nome otavio

#outra forma
print('jose "otavio"') # vai exibir as aspas em volta do nome otavio


